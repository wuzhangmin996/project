package com.wu.project20.bean;

public class Scores {
    private String totalScore ;
    private String operationScore ;
    private String choiceScore ;

    public String getTotalScore() {
        return totalScore;
    }

    public void setTotalScore(String totalScore) {
        this.totalScore = totalScore;
    }

    public String getOperationScore() {
        return operationScore;
    }

    public void setOperationScore(String operationScore) {
        this.operationScore = operationScore;
    }

    public String getChoiceScore() {
        return choiceScore;
    }

    public void setChoiceScore(String choiceScore) {
        this.choiceScore = choiceScore;
    }
}
